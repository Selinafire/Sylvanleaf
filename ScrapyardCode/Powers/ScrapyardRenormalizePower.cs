namespace Scrapyard.ScrapyardCode.Powers;

[RegisterPower]
public class ScrapyardRenormalizePower: ModPowerTemplate
{
    // 类型，Buff或Debuff
    public override PowerType Type => PowerType.Buff;
    // 叠加类型，Counter表示可叠加，Single表示不可叠加
    public override PowerStackType StackType => PowerStackType.Single;

    // 自定义图标路径。1:1即可。原版游戏大图256x256，小图64x64。
    public override PowerAssetProfile AssetProfile => new(
        IconPath: "res://Test/images/powers/test_power.png",
        BigIconPath: "res://Test/images/powers/test_power.png"
    );

    public override Task AfterApplied(Creature? creature, CardModel? card)
    {
        if (creature?.Player is { } player)
        {
            ScrapyardEnergySystem.SetSpendMode(player, ScrapyardEnergySpendMode.Subtract);
        }
        return Task.CompletedTask;
    }

    public override Task AfterRemoved(Creature oldOwner)
    {
        if (oldOwner.Player is { } player)
        {
            ScrapyardEnergySystem.ResetSpendMode(player);
        }
        return Task.CompletedTask;
    }

    public override async Task AfterSideTurnEnd(PlayerChoiceContext choiceContext, CombatSide side, IEnumerable<Creature> participants)
    {
        if (participants.Contains(Owner))
        {
            await PowerCmd.Remove(this);
        }
    }
}